#include<stdio.h>

int main(){
    // Showing various backslash constants

    printf("This is \t atg \n"); // \t--> tab (Adds space)
    printf("This is\batg \n"); // \b--> backspace (Delete Previous Character)
    printf("This is\a atg \n"); // \a--> Bell(Alert)
    // \n--> New Line Also known as escape sequence 



    return 0;
}